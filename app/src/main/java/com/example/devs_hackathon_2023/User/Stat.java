package com.example.devs_hackathon_2023.User;

public class Stat {
    private String name;
    private String value;

    public Stat(String name, String value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public String getValue() {
        return value;
    }
}
